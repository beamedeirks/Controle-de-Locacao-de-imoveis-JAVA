    public void alocar() {
        boolean teste = false;
        int cod = 0;

        cod = Integer.parseInt(jTextField1.getText());
        for (int i = 0; i < TelaMenu.Array_imoveis.size(); i++) {

            if (TelaMenu.Array_imoveis.get(i).codigo == cod) {
                teste = true;
                if (TelaMenu.Array_imoveis.get(i).status.equals("Disponível")) {
                    JOptionPane.showMessageDialog(null, "Imóvel alugado com sucesso!!\nValor do aluguel: R$" + 
                            TelaMenu.Array_imoveis.get(i).aluguel_final()+
                            "\nCódigo do Imóvel: " + TelaMenu.Array_imoveis.get(i).codigo);
                    TelaMenu.Array_imoveis.get(i).status = "Alugado";
                } else {
                    JOptionPane.showMessageDialog(null, "Imóvel já alocado!!");
                }
           }
            

        }
        if (teste == false) {
                JOptionPane.showMessageDialog(null, "Código de imóvel não encontrado!!");
            }
    }
